package com.artwell.erp.tna.entity;

import java.util.Date;

public class SampleRecord {
	private String sampleOrderNumber;
	private Date orderDate;
	private Date requiredDate;
	private Date deliveryDate;
	private Date approvalDate;
	
	public Date getOrderDate() {
		return orderDate;
	}
	public void setOrderDate(Date orderDate) {
		this.orderDate = orderDate;
	}
	public Date getRequiredDate() {
		return requiredDate;
	}
	public void setRequiredDate(Date requiredDate) {
		this.requiredDate = requiredDate;
	}
	public Date getDeliveryDate() {
		return deliveryDate;
	}
	public void setDeliveryDate(Date deliveryDate) {
		this.deliveryDate = deliveryDate;
	}
	public String getSampleOrderNumber() {
		return sampleOrderNumber;
	}
	public void setSampleOrderNumber(String sampleOrderNumber) {
		this.sampleOrderNumber = sampleOrderNumber;
	}
	public Date getApprovalDate() {
		return approvalDate;
	}
	public void setApprovalDate(Date approvalDate) {
		this.approvalDate = approvalDate;
	}
	
}
